def check_modules():
    client = paramiko.SSHClient()
    client.set_missing_host_key_policy(paramiko.AutoAddPolicy())
    client.connect(HOST, username=USER, password=PASS)
    
    print("Grepping node_modules (this might take time)...")
    # Timeout? grep might be slow.
    # Limit output
    # grep -r -m 1 ... (stop after 1 match per file)
    cmd = "grep -r -m 1 'localhost:8000' /opt/openehrcore/frontend-pwa/node_modules"
    _, stdout, stderr = client.exec_command(cmd, timeout=30)
    
    try:
        out = stdout.read().decode()
        if out:
            print("FOUND IN MODULES:")
            print(out[:1000]) # truncated
        else:
            print("Not found in node_modules.")
    except Exception as e:
        print(f"Error or Timeout: {e}")
        
    client.close()

if __name__ == "__main__":
    check_modules()
